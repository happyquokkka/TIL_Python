print(5+4) # 정수 연산 : 9
print(5.0-3.0) #실수 연산 : 2.0
print(5*4) # 정수 연산 : 20
print(5.0 * 4) # 실수 연산 (정수가 실수에 맞춘다) : 20.0
print(20 / 7) # 실수 연산 : 2.8571...
print(200 / 10) # 실수 연산 : 20.0

print(20//7) # 나눗셈 (몫) : 2
print(20%7) # 나눗셈(나머지) : 6

# 지수 계산 **
# 2의 7승을 계산
print(2**7)

# y = 3x^ + 7x + 9
x = 3
y = 3*x**2 + 7*x + 9
print(y)

