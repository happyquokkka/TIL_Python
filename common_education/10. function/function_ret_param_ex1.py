# 사칙연산 함수 작성
def add(num1, num2) :
    total = num1+num2
    print('%d + %d = %d' % (num1, num2, total))

add(9,3)

def sub(num1, num2) :
    total = num1-num2
    print('%d - %d = %d' % (num1, num2, total))

sub(9,3)

def mul(num1, num2) :
    total = num1*num2
    print('%d * %d = %d' % (num1, num2, total))

mul(9,3)

def div(num1, num2) :
    total = num1/num2
    print('%d / %d = %d' % (num1, num2, total))

div(9,3)

